// ─────────────────────────────────────────────
// 1. 입력 처리
// ─────────────────────────────────────────────
const fs = require("fs");
// 공백/줄바꿈 상관 없이 숫자들만 쭉 읽기
const data = fs.readFileSync(0, "utf8").trim().split(/\s+/).map(Number);

// ─────────────────────────────────────────────
// 2. 메모이제이션 배열 준비
// ─────────────────────────────────────────────
//
// 문제 정의:
// w(a, b, c)
//
// 1) a <= 0 or b <= 0 or c <= 0 이면 1
// 2) a > 20 or b > 20 or c > 20 이면 w(20, 20, 20)
// 3) a < b < c 이면
//    w(a, b, c-1) + w(a, b-1, c-1) - w(a, b-1, c)
// 4) 그 외의 경우
//    w(a-1, b, c) + w(a-1, b-1, c) + w(a-1, b, c-1) - w(a-1, b-1, c-1)
//
// a, b, c 가 20을 초과하면 모두 (20, 20, 20)으로 "클램프" 되므로
// 실제로 필요한 값의 범위는 [1..20] 이다.
// 따라서 0~20까지 인덱스를 갖는 3차원 배열을 만들고 메모이제이션 한다.
//
// memo[a][b][c] = w(a, b, c) (단, 0 <= a,b,c <= 20)
// 0은 아직 계산하지 않은 상태를 의미한다.
const memo = Array.from({ length: 21 }, () =>
  Array.from({ length: 21 }, () => Array(21).fill(0))
);

// ─────────────────────────────────────────────
// 3. 재귀 함수 w(a, b, c) 정의 (Top-down DP)
// ─────────────────────────────────────────────
function w(a, b, c) {
  // 1) a, b, c 중 하나라도 0 이하이면 1
  if (a <= 0 || b <= 0 || c <= 0) {
    return 1;
  }

  // 2) a, b, c 중 하나라도 20 초과이면 w(20, 20, 20)로 처리
  if (a > 20 || b > 20 || c > 20) {
    return w(20, 20, 20);
  }

  // 이제 a, b, c는 1~20 범위
  // 이미 계산된 값이면 그대로 반환
  if (memo[a][b][c] !== 0) {
    return memo[a][b][c];
  }

  let result;
  // 3) a < b < c 인 경우
  if (a < b && b < c) {
    result =
      w(a, b, c - 1) +
      w(a, b - 1, c - 1) -
      w(a, b - 1, c);
  } else {
    // 4) 그 외의 경우
    result =
      w(a - 1, b, c) +
      w(a - 1, b - 1, c) +
      w(a - 1, b, c - 1) -
      w(a - 1, b - 1, c - 1);
  }

  // 메모이제이션 배열에 저장 후 반환
  memo[a][b][c] = result;
  return result;
}

// ─────────────────────────────────────────────
// 4. 입력 케이스들에 대해 w(a, b, c) 계산 & 출력
// ─────────────────────────────────────────────
//
// 입력은 여러 줄의 a b c 가 주어지며,
// 마지막 줄은 "-1 -1 -1" 이 나올 때 종료한다.
//
// 각 줄마다
//   w(a, b, c) = 결과
// 형태로 출력해야 한다.
let out = [];
for (let i = 0; i < data.length; i += 3) {
  const a = data[i];
  const b = data[i + 1];
  const c = data[i + 2];

  // 종료 조건: -1 -1 -1
  if (a === -1 && b === -1 && c === -1) break;

  const value = w(a, b, c);
  out.push(`w(${a}, ${b}, ${c}) = ${value}`);
}

// 모든 결과를 한 번에 출력
console.log(out.join("\n"));