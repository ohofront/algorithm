// ─────────────────────────────────────────────
// 1. 입력 처리
// ─────────────────────────────────────────────
const fs = require("fs");
const input = fs.readFileSync(0, "utf8").trim().split(" ").map(Number);

const N = input[0]; // 1부터 N까지의 자연수
const M = input[1]; // 길이가 M인 수열

// ─────────────────────────────────────────────
// 2. 필요한 변수들 선언
// ─────────────────────────────────────────────

// 현재까지 선택한 수열을 저장할 배열 (길이 M)
const selected = new Array(M);
// 완성된 수열 문자열들을 모아둘 배열 (마지막에 한 번에 출력)
const result = [];

/**
 * dfs(depth, start)
 * ----------------------------------------------
 * depth : 현재 수열에서 채우고 있는 위치 (0 ~ M-1)
 * start : 이번 위치에서 선택할 수 있는 최소 값
 *
 * 조건: 비내림차순(오름차순 허용, 같거나 커야 함)
 *  -> 이전에 선택한 값보다 작은 수는 선택할 수 없음
 *  -> 그래서 for 문을 'start'부터 시작한다.
 */
function dfs(depth, start) {
  // 수열의 길이 M개를 모두 채운 경우
  if (depth === M) {
    // selected 배열을 공백으로 join 해서 문자열로 만든 뒤 결과에 저장
    result.push(selected.join(" "));
    return;
  }

  // 현재 자리(depth)에 올 수 있는 수를 순회
  for (let num = start; num <= N; num++) {
    // 현재 위치에 num 선택
    selected[depth] = num;

    // 다음 위치(depth + 1)를 채우러 재귀 호출
    // 다음에도 num 이상만 선택할 수 있도록 시작값을 num으로 전달
    dfs(depth + 1, num);
  }
}

// 깊이 우선 탐색 시작
// 첫 번째 위치(depth=0)에서는 1부터 N까지 모두 가능하므로 start=1
dfs(0, 1);

// ─────────────────────────────────────────────
// 3. 결과 출력
// ─────────────────────────────────────────────
console.log(result.join("\n"));